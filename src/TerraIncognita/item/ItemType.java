package TerraIncognita.item;

/**
 * Enum loại vật phẩm.
 */
public enum ItemType {
    POTION,         // Bình hồi phục (HP, MP...)
    WEAPON,         // Vũ khí
    ARMOR,          // Giáp
    KEY,            // Chìa khóa
    SCROLL,         // Cuộn giấy phép thuật
    MATERIAL,       // Nguyên liệu (nâng cấp)
    CONSUMABLE,     // Vật phẩm dùng 1 lần khác
    QUEST_ITEM;     // Vật phẩm nhiệm vụ (không bán/vứt được)
}
